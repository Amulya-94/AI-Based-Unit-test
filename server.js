/**
 * ==========================================
 * AI-Based Unit Test Generator
 * ==========================================
 * 
 * DESCRIPTION
 * ------------------------------------------
 * A powerful, client-side intelligent development environment designed to 
 * streamline unit testing. Built with React and powered by Google's Gemini API, 
 * this tool allows developers to:
 * 
 * 1. Write and edit JavaScript/TypeScript code with Monaco Editor.
 * 2. Automatically generate comprehensive unit tests using AI.
 * 3. Execute tests securely in a browser-based sandbox (Web Workers).
 * 4. View real-time logs and pass/fail results instantly.
 * 
 * All without a backend server.
 * 
 * --- TECH STACK ---
 * 
 * - Framework: React 19
 * - Styling: TailwindCSS
 * - Editor: Monaco Editor (VS Code core)
 * - AI: Google GenAI SDK (Gemini 1.5/2.0)
 * - Runtime: Web Workers (Sandboxed Execution)
 * - Icons: Lucide React
 * 
 * --- HOW TO RUN ---
 * 
 * 1. Open `index.html` directly in a modern browser.
 *    OR
 * 2. Serve via a static server (e.g., `npx serve .` or Python `http.server`).
 * 
 * --- CONFIGURATION ---
 * 
 * - API Key: Required for AI features. Click "Settings" in the UI to enter 
 *   your Google Gemini API Key. It is stored locally in your browser.
 */
